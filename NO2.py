#Menghitung Luas & Keliling Bangun Datar Persegi Panjang

print('Luas dan Keliling Persegi Panjang')
print('---------------------------------')

p=float(input('Masukkan panjang persegi panjang = '))
l=float(input('Masukkan lebar persegi panjang = '))

keliling=2*(p+l)
luas=p*l

print('Luas persegi panjang =',luas,'cm^2\nKeliling persegi panjang =',keliling,'cm')
